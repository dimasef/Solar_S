<p>В 2008 году наземными специалистами во избежание столкновения с астероидом Штейнс была проведена корректировка орбиты корабля, что не помешало ему сфотографировать поверхность небесного тела. На снимках ученые обнаружили более 20 кратеров диаметрами от 200 метров. В 2010-м Rosetta передала на Землю фотографии другого астероида — Лютеции. Это небесное тело оказалось планетезималью — образованием, из которых в прошлом формировались планеты. В июне 2011-го аппарат перевели в спящий режим для экономии энергии, а 20 января 2014 года Rosetta «проснулась».</p>
<h1 class="topic-title">Зонд Philae</h1>
<p>Зонд назван в честь острова Филы на реке Нил в Египте. Там находились древние культовые сооружения, а также обнаружена плита с иероглифическими записями цариц Клеопатры II и Клеопатры III. В качестве места для посадки на комету ученые выбрали участок под названием Агилика. На Земле это тоже остров на реке Нил, куда была перенесена часть древних памятников, которым угрожало подтопление в результате строительства Асуанской плотины.</p>
<p><iframe width="940" height="650" class="ig-frame b-topic_pic_940" src="//dyn.ig.rambler.ru/fily/" frameborder="0" allowfullscreen=""></iframe></p>
<p>Масса спускаемого зонда Philae — сто килограммов. Линейные размеры не превышают метра. Зонд несет на своем борту десять инструментов, необходимых для исследования ядра кометы. С помощью радиоволн ученые планируют изучить внутреннюю структуру ядра, а микрокамеры позволят сделать с поверхности кометы панорамные снимки. Сверло, установленное на Philae, поможет взять пробы грунта с глубины до 20 сантиметров.</p>
<p>Батарей Philae хватит на 60 часов автономной работы, потом питание переключится на солнечные батареи. Все данные измерений в режиме онлайн будут поступать на аппарат Rosetta, а с него — к Земле. После спуска Philae аппарат Rosetta начнет отдаляться от кометы, превратившись в ее спутник.</p>

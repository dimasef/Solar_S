<p>Средняя температура поверхности ядра кометы — минус 70 градусов Цельсия. Измерения, выполненные в рамках миссии Rosetta, показали: температура кометы слишком высока, чтобы ее ядро полностью покрывалось слоем льда. Как считают исследователи, поверхность ядра представляет собой темную пылевую корку. Тем не менее ученые не исключают, что там могут быть и ледяные участки.</p>
<p>Также установлено, что в поток газов, истекающих из комы (облака вокруг ядра кометы), входят сероводород, аммиак, формальдегид, синильная кислота, метанол, сернистый ангидрид и сероуглерод. Ранее считалось, что по мере нагревания ледяной поверхности кометы, приближающейся к Солнцу, выделяются только самые летучие соединения — двуокись и моноокись углерода.</p>

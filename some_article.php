<p>С помощью Rosetta ученые установили, что каждую секунду комета 67P/Чурюмова-Герасименко выпускает в окружающее пространство водяной пар в объеме примерно двух стаканов (по 150 миллилитров). С такими темпами комета за 100 дней  заполнила бы бассейн олимпийского размера. По мере приближения к Солнцу выброс пара только увеличивается.</p>
<p>Максимальное сближение с Солнцем произойдет 13 августа 2015 года, когда комета 67P/Чурюмова-Герасименко окажется в точке перигелия. Тогда и будет наблюдаться наиболее интенсивное испарение ее материи.</p>
<h1 class="topic-title">Космический аппарат Rosetta</h1>
<p>Космический аппарат Rosetta вместе со спускаемым зондом Philae стартовал 2 марта 2004 года на ракете-носителе семейства Ariane 5 с космодрома Куру во Французской Гвиане.</p>
<p>Название космический аппарат получил в честь розеттского камня. Расшифровка надписей на этой древней каменной плите, выполненная к 1822 году французом Жаном-Франсуа Шампольоном, позволила лингвистам совершить гигантский прорыв в изучении египетской иероглифической письменности. Подобного качественного скачка в исследовании эволюции Солнечной системы ученые ожидают и от миссии Rosetta.</p>
<p><iframe width="940" height="650" class="ig-frame b-topic_pic_940" src="//dyn.ig.rambler.ru/rozetta/" frameborder="0" allowfullscreen=""></iframe></p>
<p>Сама Rosetta — это алюминиевый ящик размерами 2,8x2,1x2,0 метров с двумя солнечными батареями по 14 метров каждая. Стоимость проекта — 1,3 миллиарда долларов, а его основным организатором выступает Европейское космическое агентство (ЕКА). Меньшее участие в нем принимают НАСА, а также национальные космические агентства других стран. Всего в проекте задействовано 50 компаний из 14 стран Европы и США. На Rosetta размещено одиннадцать научных инструментов — специальных систем из датчиков и анализаторов.</p>
<p>По ходу своего путешествия Rosetta совершила три маневра вокруг орбиты Земли и один — вокруг Марса. К орбите кометы аппарат приблизился 6 августа 2014 года. За свой долгий путь аппарат успел выполнить ряд исследований. Так, в 2007-м, пролетая мимо Марса на расстоянии тысячи километров, он передал на Землю данные о магнитном поле планеты.</p>

<p>Также благодаря миссии Rosetta астрономы обратили внимание на гантелеобразную форму ядра. Не исключено, что эта комета могла образоваться в результате столкновения пары протокомет. Вероятно, две части тела 67P/Чурюмова-Герасименко со временем разъединятся.</p>
<p>Есть и другая гипотеза, объясняющая формирование двойной структуры интенсивным испарением водяного пара в центральной части когда-то сферообразного ядра кометы.</p>

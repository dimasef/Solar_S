<p class="b-topic__announce">Европейское космическое агентство сообщило об успешной посадке зонда Philae на комету 67P/Чурюмова-Герасименко. Зонд отделился от аппарата Rosetta днем 12 ноября (по московскому времени). Rosetta же покинула Землю 2 марта 2004 и более десяти лет летела к комете. Основная цель миссии — исследование эволюции ранней Солнечной системы. В случае успеха самый амбициозный проект ЕКА может стать своего рода розеттским камнем не только астрономии, но и технологий.</p>
<h1 class="topic-title">Долгожданный гость</h1>
<p>Комета 67P/Чурюмова-Герасименко была открыта в 1969 году советским астрономом Климом Чурюмовым при исследовании фотоснимков, сделанных Светланой Герасименко. Комета относится к группе короткопериодических: период обращения вокруг Солнца — 6,6 лет. Большая полуось орбиты — чуть свыше 3,5 астрономических единиц, масса — примерно 10<sup>13</sup> килограммов, линейные размеры ядра — несколько километров.</p>
<p>Исследования таких космических тел необходимо, во-первых, для изучения эволюции кометного вещества, и, во-вторых, для понимания возможного влияния испаряющихся в комете газов на движение окружающих небесных тел. Данные, полученные с помощью миссии Rosetta, помогут объяснить процессы эволюции Солнечной системы и возникновения воды на Земле. Кроме того, ученые надеются обнаружить органические следы от L-форм («левосторонних» форм) аминокислот, являющихся основой  жизни на Земле. Если эти вещества будут найдены,  гипотеза о внеземных источниках земной органики получит новое подтверждение. Однако уже к настоящему времени благодаря проекту Rosetta астрономы узнали много интересного о самой комете.</p>
